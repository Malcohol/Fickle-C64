.macro spriterow ARG
.if DEBUG
	.byte .lobyte(.hiword(ARG)) | 128, .hibyte(ARG), .lobyte(ARG) | 1
.else
	.byte .lobyte(.hiword(ARG)), .hibyte(ARG), .lobyte(ARG)
.endif
.endmacro


